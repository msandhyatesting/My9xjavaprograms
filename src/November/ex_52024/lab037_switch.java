package November.ex_52024;

public class lab037_switch {
    public static void main(String[] args) {
        char code='c';
        switch(code)
        {
            default:
                System.out.println("Hellooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;


        }
    }
}
